public class variaveis {
    public static void main(String[] args) {
        long nlongo = 12100013; 
        // variavel do tipo numerica inteira longa
        System.out.println(nlongo);

        int n5 = 565;
        // variavel do tipo numerica a mais utilizada
        System.out.println(n5);
        
        short npequeno = 8;
        // variavel do tipo numerica inteira curta quase não utilizada
        System.out.println(npequeno);

        float valorFlutuante = 455.45f;
        // variavel do tipo numerica com casas decimais quase não utilizada (letra f obrigatoria no final) 
        System.out.println(valorFlutuante);

        double valorDouble = 465.58;
        // variavel do tipo numerica com casas decimais 
        System.out.println(valorDouble);

        String stringcliente = "Fernando Oliveira"; 
        // variavel do tipo string (letras e numeros) Não calcula
        System.out.println(stringcliente);

        boolean condicao = true;
        // variavel do tipo booleana : verdadeiro ou falso / true ou false
        System.out.println(condicao);

    }
}
